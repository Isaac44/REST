package br.com.fepi.isc.rest.model;

import androidx.annotation.NonNull;

public class PokemonUrl
{
    // ---------------------------------------------------------------------------------------------
    // Atributos
    // ---------------------------------------------------------------------------------------------

    public final String nome;

    public final String url;

    // ---------------------------------------------------------------------------------------------
    // Construtor
    // ---------------------------------------------------------------------------------------------

    public PokemonUrl(String nome, String url) {
        this.nome = nome;
        this.url = url;
    }

    // ---------------------------------------------------------------------------------------------
    // Getters
    // ---------------------------------------------------------------------------------------------

    @NonNull
    @Override
    public String toString() {
        return this.nome;
    }
}
